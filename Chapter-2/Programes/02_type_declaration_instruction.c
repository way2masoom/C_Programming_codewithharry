// Type declaration instruction in c
#include <stdio.h>

int main()
{
    // int a=4;       // Type decleration instruction
    int a = 4, b, c; // Type decleration instruction
    b = c = a;

    printf("THE value of a %d \n", a);
    printf("THE value of b %d \n", b);
    printf("THE value of c %d \n", c);

    float i = 2.2;
    float j = i + 8.9;
    printf("THE value of j %f \n", j);

    return 0;
}